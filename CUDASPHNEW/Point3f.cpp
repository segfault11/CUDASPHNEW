#include "Point3f.h"

//-----------------------------------------------------------------------------
//  Class member definitions
//-----------------------------------------------------------------------------
Point3f::Point3f ()
: mX(0.0f), mY(0.0f), mZ(0.0f)
{

}
//-----------------------------------------------------------------------------
Point3f::Point3f (float x, float y, float z)
: mX(x), mY(y), mZ(z)
{
    
}
//-----------------------------------------------------------------------------
Point3f::~Point3f ()
{

}
//-----------------------------------------------------------------------------
Point3f::Point3f (const Point3f& orig)
{
    mX = orig.mX;
    mY = orig.mY;
    mZ = orig.mZ;
}
//-----------------------------------------------------------------------------
Point3f& Point3f::operator= (const Point3f& orig)
{
    mX = orig.mX;
    mY = orig.mY;
    mZ = orig.mZ;

    return *this;    
}
